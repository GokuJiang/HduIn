import UIKit
extension UIColor {
    class var HIWarmGrey: UIColor {
        return UIColor(white: 122.0 / 255.0, alpha: 1.0)
    }
    
    class var HIWhite: UIColor {
        return UIColor(white: 255.0 / 255.0, alpha: 1.0)
    }
    
    class var HISapphire: UIColor {
        return UIColor(red: 55.0 / 255.0, green: 47.0 / 255.0, blue: 160.0 / 255.0, alpha: 1.0)
    }
    
    class var HICornflower: UIColor {
        return UIColor(red: 110.0 / 255.0, green: 155.0 / 255.0, blue: 244.0 / 255.0, alpha: 1.0)
    }
}